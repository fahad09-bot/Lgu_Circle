package com.codesses.lgucircle.model;

public class Event {

    String e_id, info, date, department, image;
    int type;

    public String getE_id() {
        return e_id;
    }

    public String getInfo() {
        return info;
    }

    public String getDate() {
        return date;
    }

    public String getDepartment() {
        return department;
    }

    public String getImage() {
        return image;
    }

    public int getType() {
        return type;
    }
}
